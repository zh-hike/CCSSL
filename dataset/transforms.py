class RandAugmentPC(object):
    def __init__(self, n, m):
        assert n >= 1
        assert 1 <= m <= 10
        self.n = n
        self.m = m
        self.augment_pool = my_augment_pool()

    def __call__(self, img):
        ops = random.choices(self.augment_pool, k=self.n)
        for op, max_v, bias in ops:
            prob = np.random.uniform(0.2, 0.8)
            if random.random() + prob >= 1:
                img = op(img, v=self.m, max_v=max_v, bias=bias)
        img = CutoutAbs(img, int(32*0.5))
        return img


class RandAugmentMC(object):
    def __init__(self, n, m):
        assert n >= 1
        assert 1 <= m <= 10
        self.n = n
        self.m = m
        self.augment_pool = fixmatch_augment_pool()

    def __call__(self, img):
        ops = random.choices(self.augment_pool, k=self.n)
        for op, max_v, bias in ops:
            v = np.random.randint(1, self.m)
            if random.random() < 0.5:
                img = op(img, v=v, max_v=max_v, bias=bias)
        img = CutoutAbs(img, int(32 * 0.5))
        return img


class RandomApply:
    def __init__(self, p, transforms):
        self.p = p
        ts = []
        for t in transforms:
            for key in t.keys():
                ts.append(eval(key)(**t[key]))

        self.trans = T.Compose(ts)

    def __call__(self, img):
        return self.trans(img)

class BaseTransform:
    def __init__(self, cfg) -> None:
        """
        Args:
            cfg: list [dict, dict, dict]
        """
        ts = []
        for op in cfg:
            name = list(op.keys())[0]
            if op[name] is None:
                ts.append(eval(name)())
            else:
                ts.append(eval(name)(**(op[name])))

        self.t = T.Compose(ts)

    def __call__(self, img):
        
        return self.t(img)


class ListTransform:
    def __init__(self, ops) -> None:
        """
        Args:
            ops: list[list[dict, dict], ...]
        """
        self.ts = []
        for op in ops:
            self.ts.append(BaseTransform(op))

    def __call__(self, img):
        results = []
        for op in self.ts:
            results.append(op(img))
        return results

